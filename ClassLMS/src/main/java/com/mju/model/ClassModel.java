package com.mju.model;

public class ClassModel {
	int id;
	String name;
	int prof_id;
	
	public int getId(){
		return id;
	}
	
	public void setId(int id){
		this.id = id;
	}
	
	public String getName(){
		return name;
	}
	
	public void setName(String name){
		this.name = name;
	}
	
	public int getProf_id(){
		return prof_id;
	}
	
	public void setProf_id(int prof_id){
		this.prof_id = prof_id;
	}
	
}
